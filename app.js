const BN = web3.utils.BN;

async function main() {
  const factory = new web3.eth.Contract(factoryAbi, factoryAddr);
  const swap = new web3.eth.Contract(swapAbi, swapAddr);

  const tokenInContract = new web3.eth.Contract(erc20Abi, inTokenAddr)
  const tokenOutContract = new web3.eth.Contract(erc20Abi, outTokenAddr)


  while (true) {
    const pairAddr = await factory.methods
      .getPair(inTokenAddr, outTokenAddr)
      .call();
    console.log(pairAddr);
    if (pairAddr != "0x0000000000000000000000000000000000000000") {
      console.log("bingo pair")

      while(true){
        
        const balanceInPool = new BN(await tokenInContract.methods.balanceOf(pairAddr).call())
        console.log(new Date(), balanceInPool.toString())
        const isNotZero = balanceInPool.gt(new BN('1234'))
        if(isNotZero){
          console.log("non zero")
          const swapTx = await swap.methods.swapExactTokensForTokens(
            "100000000000000000000",
            0,
            [
              inTokenAddr,
              outTokenAddr,
            ],
            myAddr,
            // currentBlock + 10
            99999999999
          );
    
          const swapSigned = await web3.eth.accounts.signTransaction(
            {
              to: swapAddr,
              data: swapTx.encodeABI(),
              gas: "5000000",
              gasPrice: "5000000001",
            },
            yourPrivateKey
          );
    
          const swapResponse = await web3.eth.sendSignedTransaction(
            swapSigned.rawTransaction
          );
    
          console.log(swapResponse)
          return
  
        }
      }
      


    }
  }
}

